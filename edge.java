package PrimMST;

public class edge {
	int vertex1;
	int vertex2;
	int cost;
	int numberOfVertex;
	public edge(int vertex1, int vertex2, int cost, int numberOfVertex) {
		this.vertex1 = vertex1;
		this.vertex2 = vertex2;
		this.cost = cost;
		this.numberOfVertex = numberOfVertex;
	}
}
